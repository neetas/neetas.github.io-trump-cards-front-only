function Cardfadein(){
    $("#name").fadeIn("slow");
    $("#image").fadeIn("slow");
    $("#metrics").fadeIn("slow");
    $("#score").fadeIn("swing");
}

function hideCard(){
    $("#name").hide();
    $("#image").hide();
    $("#metrics").hide();
    $("#score").hide();
}


function Chartfadein(){
	$(".top10").fadeIn("slow");
    $(".chart").fadeIn("slow");
}

function chartHide(){
	$(".top10").hide();
    $(".chart").hide();
}